
# Muestra un mensaje de Hola Mundo
print('Hola Mundo')  # Hola Mundo

# Define Variables
nombre = 'Joel'
edad = 33

# Imprime por consola los valores de las variables creados
print(nombre)  # Joel
print(edad)  # 33

# Imprime una concatenación de las dos variables
print('Nombre: ', nombre, '\nEdad:', edad)
'''
Nombre: Joel
Edad: 33
'''

# Imprime el mismo valor de la cadena de texto de una manera más simple
print(f'Nombre: {nombre} \nEdad: {edad}')
'''
Nombre: Joel
Edad: 33
'''
