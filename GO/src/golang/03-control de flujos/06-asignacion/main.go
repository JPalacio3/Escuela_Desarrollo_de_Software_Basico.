package main

import "fmt"

func main() {

	a := 2

	// Operación de suma
	// a = a + 2

	// Operadores de asiganción

	// Suma en asignación
	a += 2 // 4
	fmt.Println(a)

	// Resta en asignación
	a -= 2 // 2
	fmt.Println(a)

	//Multiplicación en asignación
	a *= 4 // 8
	fmt.Println(a)

	// División en asignación
	a /= 2 // 4
	fmt.Println(a)

	// Módulo en asignación
	a %= 2 // 0
	fmt.Println(a)

}
